    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String rollNo = jTextField1.getText();
        int cMarks = Integer.parseInt(jTextField2.getText());
        int osMarks = Integer.parseInt(jTextField3.getText());
        int cnMarks = Integer.parseInt(jTextField4.getText());
        int dbmsMarks = Integer.parseInt(jTextField5.getText());
        int javaMarks = Integer.parseInt(jTextField6.getText());
        
        int result = 0;
        result = cMarks+osMarks+cnMarks+dbmsMarks+javaMarks;
        
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            st.execute("INSERT INTO RESULT VALUES('"+rollNo+"', '"+cMarks+"', '"+osMarks+"','"+cnMarks+"','"+dbmsMarks+"','"+javaMarks+"','"+result+"')");
            JOptionPane.showMessageDialog(null, "Marks sucessfully added");
            setVisible(false);
            AddResult frame = new AddResult();
            frame.setVisible(true);
            frame.setLocationRelativeTo(null);
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String rollNo = jTextField1.getText();
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = st.executeQuery("SELECT * FROM STUDENT WHERE ROLLNO= '"+rollNo+"'");
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            if(!rs.first()){
                JOptionPane.showMessageDialog(null, "Roll Number not found");
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

